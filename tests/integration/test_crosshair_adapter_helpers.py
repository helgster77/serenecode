class TestCliTargetReference:
    def test_dotted_module(self) -> None:
        """Non-absolute module path → uses dotted module.function."""
        def my_func() -> int:
            return 1
        result = _cli_target_reference("serenecode.config", "my_func", my_func)
        assert result == "serenecode.config.my_func"

    def test_absolute_file_with_line_number(self, tmp_path: Path) -> None:
        """Branch (lines 135-136): absolute .py file + lookup function source line."""
        f = tmp_path / "mod.py"
        f.write_text("def my_func():\n    return 1\n", encoding="utf-8")
        # Import the function to get a real callable with a source line
        import importlib.util
        spec = importlib.util.spec_from_file_location("mod", str(f))
        assert spec is not None
        module = importlib.util.module_from_spec(spec)
        assert spec.loader is not None
        spec.loader.exec_module(module)
        my_func = module.my_func
        result = _cli_target_reference(str(f), "my_func", my_func)
        # Should be of the form "<absolute path>:<line>"
        assert result.startswith(str(f))
        assert ":1" in result or ":2" in result

    def test_falls_back_to_dotted_when_source_unavailable(self) -> None:
        """Lambda has no source line (or rather, may not). Falls back."""
        def my_func() -> int:
            return 1
        result = _cli_target_reference("non/abs/path.py", "my_func", my_func)
        assert "my_func" in result

    def test_absolute_file_with_oserror_on_source_lookup(
        self,
        tmp_path: Path,
        monkeypatch: pytest.MonkeyPatch,
    ) -> None:
        """Branch (lines 135-136): inspect.getsourcelines raises → fallback line lookup."""
        import inspect
        f = tmp_path / "mod.py"
        f.write_text("def my_func():\n    return 1\n", encoding="utf-8")
        import importlib.util
        spec = importlib.util.spec_from_file_location("mod_oserr", str(f))
        assert spec is not None
        module = importlib.util.module_from_spec(spec)
        assert spec.loader is not None
        spec.loader.exec_module(module)
        my_func = module.my_func

        # Force inspect.getsourcelines to raise
        def raising_getsourcelines(obj: object) -> tuple[list[str], int]:
            raise OSError("simulated source-not-found")

        monkeypatch.setattr(inspect, "getsourcelines", raising_getsourcelines)
        result = _cli_target_reference(str(f), "my_func", my_func)
        # Should still return a valid reference (falls back to co_firstlineno)
        assert result.startswith(str(f))

    def test_absolute_file_with_no_line_number_falls_back_to_dotted(
        self,
        tmp_path: Path,
        monkeypatch: pytest.MonkeyPatch,
    ) -> None:
        """Branch: line_number is None → fall through to dotted form."""
        import inspect
        f = tmp_path / "mod.py"
        f.touch()

        def my_func() -> int:
            return 1

        # Force getsourcelines to raise AND remove __code__ so the fallback returns None
        def raising_getsourcelines(obj: object) -> tuple[list[str], int]:
            raise OSError("nope")

        monkeypatch.setattr(inspect, "getsourcelines", raising_getsourcelines)

        class _NoCode:
            pass

        # Wrap func so __code__ is not accessible
        no_code = _NoCode()
        no_code.__name__ = "my_func"  # type: ignore[attr-defined]
        result = _cli_target_reference(str(f), "my_func", no_code)  # type: ignore[arg-type]
        # line_number ends up None → falls through to dotted form
        assert result.endswith(".my_func")


