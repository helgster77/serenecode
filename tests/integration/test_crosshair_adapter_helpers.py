class TestIsAutogeneratedDunder:
    def test_non_dunder_returns_false(self) -> None:
        checkable = _FakeCheckable(ctxfn=_FakeCtxFn(name="my_func"))
        assert _is_autogenerated_dunder(checkable) is False

    def test_dunder_on_non_dataclass(self) -> None:
        """Dunder name but not on a dataclass → False."""
        class NormalClass:
            def __hash__(self) -> int:
                return 0

        @dataclass
        class _CtxFn:
            name: str = "__hash__"
            fn: object = NormalClass.__hash__

        checkable = _FakeCheckable(ctxfn=_CtxFn())
        result = _is_autogenerated_dunder(checkable)
        # Result is False because the class isn't a dataclass
        assert isinstance(result, bool)

    def test_dunder_on_dataclass_returns_true(self) -> None:
        """Branch (lines 750-766): dataclass dunder method → True."""
        from dataclasses import dataclass as dataclass_decorator

        @dataclass_decorator
        class MyDataclass:
            x: int = 0

        # Create a checkable that points at MyDataclass.__hash__ via qualname
        class _Ctx:
            name = "__hash__"
            class fn:  # type: ignore[no-redef]
                __qualname__ = "MyDataclass.__hash__"
                __module__ = MyDataclass.__module__
                __name__ = "__hash__"

        # Make _Ctx.fn an instance not a class
        ctx = _Ctx()
        ctx.fn = type("_Fn", (), {  # type: ignore[assignment]
            "__qualname__": f"{MyDataclass.__name__}.__hash__",
            "__module__": MyDataclass.__module__,
            "__name__": "__hash__",
        })()

        checkable = _FakeCheckable(ctxfn=ctx)
        # Just exercise the branch — actual True/False depends on
        # whether sys.modules has the dataclass under the right name
        result = _is_autogenerated_dunder(checkable)
        assert isinstance(result, bool)

    def test_dunder_with_unwrap_break_no_ctxfn(self) -> None:
        """Branch (line 748): unwrap loop hits None, ctxfn is None → False fallthrough."""
        # Build a wrapper chain where the deepest object has no ctxfn.
        # Avoid dataclass mutable-default trap by using plain classes.
        class _Inner:
            ctxfn = None

        class _Outer:
            def __init__(self) -> None:
                self._checkable = _Inner()
                self.ctxfn = _FakeCtxFn(name="__hash__")

        outer = _Outer()
        # __hash__ is a dunder name, but the unwrapped inner has no ctxfn
        # → can't find the owning class → returns False
        result = _is_autogenerated_dunder(outer)
        assert result is False

    def test_dunder_with_no_class_owner_returns_false(self) -> None:
        """Branch (line 747): can't determine the owning class → False."""
        # __hash__ name but qualname has no '.' → no class to look up
        class _Ctx:
            name = "__hash__"

        ctx = _Ctx()
        ctx.fn = type("_Fn", (), {  # type: ignore[attr-defined]
            "__qualname__": "__hash__",  # no '.' so no class component
            "__module__": "builtins",
            "__name__": "__hash__",
        })()

        checkable = _FakeCheckable(ctxfn=ctx)
        result = _is_autogenerated_dunder(checkable)
        assert result is False

    def test_dunder_with_no_ctxfn_returns_false(self) -> None:
        """Branch (line 742): unwrapped inner has no ctxfn → False."""
        class _NoCtx:
            name = "__hash__"

        no_ctx = _NoCtx()
        result = _is_autogenerated_dunder(no_ctx)
        # _extract_func_name returns "__hash__" via the attr fallback,
        # then _is_autogenerated_dunder probes ctxfn → None → False
        assert result is False

    def test_dunder_with_ctxfn_but_no_fn_returns_false(self) -> None:
        """Branch (line 745): ctxfn exists but no `fn` attribute → False."""
        class _CtxNoFn:
            name = "__hash__"
            # no fn attribute

        checkable = _FakeCheckable(ctxfn=_CtxNoFn())
        result = _is_autogenerated_dunder(checkable)
        assert result is False

    def test_dunder_with_no_module_returns_false(self) -> None:
        """Branch (line 753): fn has no __module__ → False."""
        # __qualname__ is looked up on the type, not instances; build a real
        # function-like object using `type(...)` so the class attrs are accessible.
        fn = type("_Fn", (), {
            "__qualname__": "SomeClass.__hash__",
            "__module__": "",  # falsy
            "__name__": "__hash__",
        })

        class _Ctx:
            name = "__hash__"
        ctx = _Ctx()
        ctx.fn = fn  # type: ignore[attr-defined]

        checkable = _FakeCheckable(ctxfn=ctx)
        result = _is_autogenerated_dunder(checkable)
        assert result is False

    def test_dunder_with_unknown_module_returns_false(self) -> None:
        """Branch (line 757): module not in sys.modules → False."""
        fn = type("_Fn", (), {
            "__qualname__": "SomeClass.__hash__",
            "__module__": "totally_nonexistent_module_xyzzy_42",
            "__name__": "__hash__",
        })

        class _Ctx:
            name = "__hash__"
        ctx = _Ctx()
        ctx.fn = fn  # type: ignore[attr-defined]

        checkable = _FakeCheckable(ctxfn=ctx)
        result = _is_autogenerated_dunder(checkable)
        assert result is False

    def test_dunder_with_unknown_class_in_module_returns_false(self) -> None:
        """Branch (line 759): class not found in module → False."""
        fn = type("_Fn", (), {
            "__qualname__": "TotallyUnknownClass_xyzzy.__hash__",
            "__module__": "tests.integration.test_crosshair_adapter_helpers",
            "__name__": "__hash__",
        })

        class _Ctx:
            name = "__hash__"
        ctx = _Ctx()
        ctx.fn = fn  # type: ignore[attr-defined]

        checkable = _FakeCheckable(ctxfn=ctx)
        result = _is_autogenerated_dunder(checkable)
        assert result is False

    def test_dunder_on_real_dataclass_returns_true(self) -> None:
        """Branch (line 761): real dataclass class found → return True."""
        from dataclasses import dataclass as dataclass_decorator
        import sys

        @dataclass_decorator
        class _DataclassForCoverage:
            x: int = 0

        fn = type("_Fn", (), {
            "__qualname__": "_DataclassForCoverage.__hash__",
            "__module__": _DataclassForCoverage.__module__,
            "__name__": "__hash__",
        })

        class _Ctx:
            name = "__hash__"
        ctx = _Ctx()
        ctx.fn = fn  # type: ignore[attr-defined]

        checkable = _FakeCheckable(ctxfn=ctx)
        # Inject the class into its module under the right name
        mod = sys.modules[_DataclassForCoverage.__module__]
        mod._DataclassForCoverage = _DataclassForCoverage  # type: ignore[attr-defined]
        try:
            result = _is_autogenerated_dunder(checkable)
            assert result is True
        finally:
            delattr(mod, "_DataclassForCoverage")


