class TestCheckCrosshairCli:
    def test_returns_bool(self) -> None:
        result = _check_crosshair_cli()
        assert isinstance(result, bool)

    def test_handles_subprocess_failure(self, monkeypatch: pytest.MonkeyPatch) -> None:
        """Branch (lines 69-70): subprocess raises → cache False."""
        # Reset the cache first
        from serenecode.adapters import crosshair_adapter
        monkeypatch.setattr(crosshair_adapter, "_CROSSHAIR_CLI_AVAILABLE", None)

        def fake_run(*args: object, **kwargs: object) -> None:
            raise FileNotFoundError("crosshair not found")

        monkeypatch.setattr(subprocess, "run", fake_run)
        result = _check_crosshair_cli()
        assert result is False

    def test_returns_cached_result(self, monkeypatch: pytest.MonkeyPatch) -> None:
        """Branch (line 56-57): cached result returned without subprocess call."""
        from serenecode.adapters import crosshair_adapter
        # Reset to None first so the next call populates it
        monkeypatch.setattr(crosshair_adapter, "_CROSSHAIR_CLI_AVAILABLE", None)
        # First call: populates the cache
        first = _check_crosshair_cli()
        assert isinstance(first, bool)
        # Second call: hits the cached-return branch (line 56-57)
        called = {"count": 0}
        original_run = subprocess.run

        def counting_run(*args: object, **kwargs: object) -> object:
            called["count"] += 1
            return original_run(*args, **kwargs)

        monkeypatch.setattr(subprocess, "run", counting_run)
        second = _check_crosshair_cli()
        assert second == first
        # No subprocess call because the cache was already populated
        assert called["count"] == 0


